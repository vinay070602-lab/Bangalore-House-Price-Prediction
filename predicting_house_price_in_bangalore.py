def convert_str_to_float(Price_str):
  if "Cr" in Price_str:
    return float(Price_str.replace("Cr", "")) * 10000000
  elif "L" in Price_str:
    return float(Price_str.replace("Lac", "")) * 100000

df1["price_rupees"] = df1["price_rupees"].apply(convert_str_to_float)

